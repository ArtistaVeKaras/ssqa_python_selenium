# list data types
# list area mutable changeable sequenced data types or arrays in othe languages

countries = ["United States", "Canada", "India", "United Kingdom"]
print(countries)

# add a new item to the list
countries.append('Japan')
print(countries)
print(len(countries))

# remove an item from the list
countries.remove('India')
print(countries)
print(len(countries))
